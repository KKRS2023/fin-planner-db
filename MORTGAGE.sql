DROP TABLE MORTGAGE;

CREATE TABLE MORTGAGE(
MORTGAGE_ID INT AUTO_INCREMENT NOT NULL PRIMARY KEY,
ACCOUNT_NUMBER INT,
SSN VARCHAR(8),
LOAN_AMOUNT DECIMAL(10,2),
CURRENT_LOAN DECIMAL(10,2),
TOTAL_TENURE INT,
STAR_DATE DATE,
EMI DECIMAL(10,2)
) AUTO_INCREMENT=500000;

